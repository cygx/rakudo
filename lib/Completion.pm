    # XXX take $pos into account
    our sub complete(Str $line, Int $pos) returns List is export {
        # XXX for debugging
        CATCH {
            default {
                say "uh-oh: $_";
            }
        }

        my $m = $line ~~ /^$<prefix>=[.*?] <|w>$<last-word>=[\w*]$/;

        my $prefix    = $m ?? ~$m<prefix>    !! '';
        my $last-word = $m ?? ~$m<last-word> !! '';

        @COMPLETIONS.grep(/^"$last-word"/).map({ $prefix ~ $_ })
    }
